    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        if(txtName.getText().equals("")|| txtTel.getText().equals("")|| txtEmail.getText().equals("")) //kiem tra trong text có giá tri không
        {
            JOptionPane.showMessageDialog (this, "Nhập thiếu thông tin","Thông báo",JOptionPane.WARNING_MESSAGE);
        }else
        {
        if(them){
            cu.addCustomer(txtName.getText(),txtTel.getText(),txtEmail.getText());
            LoadDSCustomer();
        }else
        {
            cu.editCustomer(Integer.parseInt(txtID.getText()),txtName.getText(),txtTel.getText(),txtEmail.getText());
            LoadDSCustomer();
        }
            clearForm();
        }
    }//GEN-LAST:event_btnSaveActionPerformed

