    public void Loadtotalprice(){
        rs=or.loadtotalprice();
        Vector item=null;
        txtCount.setEnabled(false);
        try
        {
            while(rs.next())
            {
               txtCount.setText(rs.getString("total"));
            }
        } catch (SQLException ex)
        {
            System.out.println(ex.toString());
        }
    }
